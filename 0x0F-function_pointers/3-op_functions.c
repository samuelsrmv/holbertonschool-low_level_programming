#include "calc.h"
/**
 * op_add - add two numbers
 * @a: first value
 * @b: second value
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub two numbers
 * @a: first value
 * @b: second value
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mult two numbers
 * @a: first value
 * @b: second value
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - add two numbers
 * @a: first value
 * @b: second value
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod two numbers
 * @a: first value
 * @b: second value
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
